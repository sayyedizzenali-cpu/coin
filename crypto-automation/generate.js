// ── COLORS ───────────────────────────────────────────────────────
const c = {
  green:  (t) => `\x1b[32m${t}\x1b[0m`,
  red:    (t) => `\x1b[31m${t}\x1b[0m`,
  yellow: (t) => `\x1b[33m${t}\x1b[0m`,
  blue:   (t) => `\x1b[34m${t}\x1b[0m`,
  bold:   (t) => `\x1b[1m${t}\x1b[0m`,
  dim:    (t) => `\x1b[2m${t}\x1b[0m`,
}

// ── HELPERS ───────────────────────────────────────────────────────
function fetchJSON(url) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers: { 'Accept': 'application/json', 'User-Agent': 'CryptoWorld/1.0' } }, (res) => {
      let data = ''
      res.on('data', chunk => data += chunk)
      res.on('end', () => {
        try { resolve(JSON.parse(data)) }
        catch(e) { reject(new Error('JSON parse failed: ' + data.slice(0, 100))) }
      })
    }).on('error', reject)
  })
}

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms))
}

function log(emoji, msg, color = 'green') {
  console.log(`${emoji}  ${c[color](msg)}`)
}

function progress(current, total, label) {
  const pct = Math.round((current / total) * 100)
  const bar = '█'.repeat(Math.floor(pct / 5)) + '░'.repeat(20 - Math.floor(pct / 5))
  process.stdout.write(`\r   ${bar} ${pct}% ${c.dim(`(${current}/${total}) ${label}`)}`)
  if (current === total) console.log()
}

function ensureDir(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true })
}

function writeFile(filePath, content) {
  ensureDir(path.dirname(filePath))
  fs.writeFileSync(filePath, content, 'utf8')
}

function readJSON(filePath) {
  if (!fs.existsSync(filePath)) return null
  try { return JSON.parse(fs.readFileSync(filePath, 'utf8')) }
  catch { return null }
}

function writeJSON(filePath, data) {
  ensureDir(path.dirname(filePath))
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8')
}

// ── DATA CACHE ────────────────────────────────────────────────────
const CACHE_DIR = './.cache'
const CACHE_FILE = `${CACHE_DIR}/coins.json`

async function getCoinsList(max = 500) {
  const cached = readJSON(CACHE_FILE)
  if (cached && cached.timestamp && Date.now() - cached.timestamp < 3600000) {
    log('💾', `Using cached coin list (${cached.coins.length} coins)`, 'blue')
    return cached.coins
  }

  log('🌐', 'Fetching coin list from CoinGecko...', 'yellow')
  const allCoins = []
  const pages = Math.ceil(max / 250)

  for (let page = 1; page <= pages; page++) {
    try {
      const limit = page === pages ? max % 250 || 250 : 250
      const coins = await fetchJSON(
        `${CONFIG.coingeckoBase}/coins/markets?vs_currency=usd&order=market_cap_desc&per_page=${limit}&page=${page}&sparkline=false`
      )
      allCoins.push(...coins)
      log('✓', `Page ${page}/${pages}: ${coins.length} coins fetched`, 'green')
      if (page < pages) await sleep(CONFIG.delayMs)
    } catch(e) {
      log('⚠', `Page ${page} failed: ${e.message}`, 'yellow')
      break
    }
  }

  writeJSON(CACHE_FILE, { timestamp: Date.now(), coins: allCoins })
  log('💾', `Cached ${allCoins.length} coins`, 'green')
  return allCoins
}

