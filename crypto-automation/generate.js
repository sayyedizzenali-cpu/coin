// 8. Git deploy
async function deploy(message = 'Auto update pages') {
  const { execSync } = require('child_process')
  log('🚀', 'Deploying to GitHub...', 'blue')

  try {
    execSync('git add .', { stdio: 'inherit' })
    execSync(`git commit -m "${message} - ${new Date().toLocaleDateString()}"`, { stdio: 'inherit' })
    execSync('git push origin main', { stdio: 'inherit' })
    log('✅', 'Deployed! Vercel will auto-redeploy in 1-2 minutes', 'green')
  } catch(e) {
    log('⚠', 'Git push failed — may already be up to date', 'yellow')
  }
}

// ── MAIN ──────────────────────────────────────────────────────────
async function main() {
  const command = process.argv[2] || 'status'

  console.log('\n' + c.bold(c.blue('🦎 CryptoWorld Page Generator')))
  console.log(c.dim('   Automated page generation system\n'))

  if (command === 'status') {
    showStatus()
    return
  }

  if (command === 'deploy') {
    const msg = process.argv[3] || 'Auto update'
    await deploy(msg)
    return
  }

  // Fetch coins for generation
  const coins = await getCoinsList(CONFIG.maxCoins)
  console.log(c.green(`\n✓ ${coins.length} coins loaded\n`))

  const startTime = Date.now()

  if (command === 'all' || command === 'coins') {
    await generateCoinPages(coins)
  }
  if (command === 'all' || command === 'buy') {
    await generateBuyPages(coins)
  }
  if (command === 'all' || command === 'price') {
    await generatePricePages(coins)
  }
  if (command === 'all' || command === 'prediction') {
    await generatePredictionPages(coins)
  }
  if (command === 'all' || command === 'compare') {
    await generateComparePages(coins)
  }
  if (command === 'all' || command === 'sitemap') {
    await generateSitemap(coins)
  }

  const elapsed = ((Date.now() - startTime) / 1000).toFixed(1)

  console.log('\n' + c.bold('═'.repeat(50)))
  console.log(c.green(c.bold(`  ✅ Done in ${elapsed}s`)))

  if (command === 'all') {
    const total = coins.length + (50 * 30) + (100 * 12) + (100 * 8) + 190 + 64
    console.log(c.green(`  📄 ~${total.toLocaleString()} pages configured`))
    console.log(c.dim('\n  Now run: node generate.js deploy'))
  }
  console.log(c.bold('═'.repeat(50)) + '\n')
}

main().catch(e => {
  console.error(c.red('\n❌ Error: ' + e.message))
  process.exit(1)
})
