// 5. Generate compare pages
async function generateComparePages(coins) {
  const topCoins = coins.slice(0, 20)
  const pairs = []

  // Generate all combinations
  for (let i = 0; i < topCoins.length; i++) {
    for (let j = i + 1; j < topCoins.length; j++) {
      pairs.push(`${topCoins[i].id}-vs-${topCoins[j].id}`)
    }
  }

  log('🔄', `Compare pages: ${pairs.length} pairs from top 20 coins`, 'blue')

  const comparePath = `${CONFIG.siteDir}/app/compare/[pair]/page.js`
  if (!fs.existsSync(comparePath)) {
    log('❌', 'compare page not found!', 'red')
    return
  }

  let content = fs.readFileSync(comparePath, 'utf8')
  const pairsList = pairs.map(p => `'${p}'`).join(',\n    ')

  const newStaticParams = `export async function generateStaticParams() {
  // Auto-generated by generate.js — ${new Date().toISOString()}
  // ${pairs.length} comparison pairs
  const topPairs = [
    ${pairsList}
  ]
  return topPairs.map(pair => ({ pair }))
}`

  content = content.replace(
    /export async function generateStaticParams\(\) \{[\s\S]*?\n\}/,
    newStaticParams
  )

  fs.writeFileSync(comparePath, content, 'utf8')
  log('✅', `Compare pages: ${pairs.length} pairs updated`, 'green')
}

// 6. Generate sitemap
async function generateSitemap(coins) {
  log('🔄', 'Generating sitemap...', 'blue')

  const BASE = process.env.NEXT_PUBLIC_SITE_URL || 'https://your-domain.com'
  const COUNTRIES = ['pakistan','india','united-states','united-kingdom','nigeria','uae','turkey','brazil','indonesia','philippines','south-africa','canada','australia','saudi-arabia','kenya','ghana','egypt','malaysia','singapore','thailand','bangladesh','vietnam','south-korea','japan','germany','france','spain','mexico','argentina','sri-lanka']
  const CURRENCIES = ['pkr','inr','aed','sar','gbp','eur','try','brl','jpy','cad','aud','myr']
  const YEARS = ['2025','2026','2027','2028','2029','2030','2035','2040']

  const urls = [`${BASE}`, `${BASE}/trending`, `${BASE}/glossary`, `${BASE}/about`]

  // Coin pages
  coins.forEach(c => urls.push(`${BASE}/coins/${c.id}`))

  // Buy pages (top 50)
  coins.slice(0, 50).forEach(c => {
    COUNTRIES.forEach(country => urls.push(`${BASE}/buy/${c.id}/${country}`))
  })

  // Price pages (top 100)
  coins.slice(0, 100).forEach(c => {
    CURRENCIES.forEach(cur => urls.push(`${BASE}/price/${c.id}/${cur}`))
  })

  // Prediction pages (top 100)
  coins.slice(0, 100).forEach(c => {
    YEARS.forEach(year => urls.push(`${BASE}/prediction/${c.id}/${year}`))
  })

  // Compare pages (top 20)
  const top20 = coins.slice(0, 20)
  for (let i = 0; i < top20.length; i++) {
    for (let j = i + 1; j < top20.length; j++) {
      urls.push(`${BASE}/compare/${top20[i].id}-vs-${top20[j].id}`)
    }
  }

  log('✅', `Sitemap: ${urls.length.toLocaleString()} URLs generated`, 'green')

  // Write URL list for reference
  writeFile('.cache/sitemap-urls.txt', urls.join('\n'))
  log('✅', `URLs saved to .cache/sitemap-urls.txt`, 'green')

  return urls.length
}

