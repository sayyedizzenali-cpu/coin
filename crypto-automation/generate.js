// 1. Update generateStaticParams in coins page
async function generateCoinPages(coins) {
  log('🔄', `Updating coin pages for ${coins.length} coins...`, 'blue')

  const coinPagePath = `${CONFIG.siteDir}/app/coins/[slug]/page.js`
  if (!fs.existsSync(coinPagePath)) {
    log('❌', 'coins/[slug]/page.js not found!', 'red')
    return
  }

  let content = fs.readFileSync(coinPagePath, 'utf8')

  // Build static params list
  const topCoins = coins.slice(0, Math.min(coins.length, 100))
  const coinIds = topCoins.map(c => `'${c.id}'`).join(',\n    ')

  // Replace generateStaticParams
  const newStaticParams = `export async function generateStaticParams() {
  // Auto-generated by generate.js — DO NOT EDIT MANUALLY
  // Generated: ${new Date().toISOString()}
  // Total: ${topCoins.length} coins pre-built, rest via ISR
  const coins = [
    ${coinIds}
  ]
  return coins.map(id => ({ slug: id }))
}`

  // Replace the function
  content = content.replace(
    /export async function generateStaticParams\(\) \{[\s\S]*?\n\}/,
    newStaticParams
  )

  fs.writeFileSync(coinPagePath, content, 'utf8')
  log('✅', `Updated coin page with ${topCoins.length} static params`, 'green')

  // Generate data file for static generation
  const dataPath = `${CONFIG.siteDir}/data/coins.json`
  writeJSON(dataPath, {
    generated: new Date().toISOString(),
    total: coins.length,
    coins: coins.map(c => ({
      id: c.id,
      name: c.name,
      symbol: c.symbol,
      rank: c.market_cap_rank,
      price: c.current_price,
      marketCap: c.market_cap,
      image: c.image,
      change24h: c.price_change_percentage_24h,
    }))
  })
  log('✅', `Saved coins data to src/data/coins.json`, 'green')
}

// 2. Generate buy pages static params
async function generateBuyPages(coins) {
  const COUNTRIES = [
    'pakistan', 'india', 'united-states', 'united-kingdom', 'nigeria',
    'uae', 'turkey', 'brazil', 'indonesia', 'philippines',
    'south-africa', 'canada', 'australia', 'saudi-arabia', 'kenya',
    'ghana', 'egypt', 'malaysia', 'singapore', 'thailand',
    'bangladesh', 'vietnam', 'south-korea', 'japan', 'germany',
    'france', 'spain', 'mexico', 'argentina', 'sri-lanka',
  ]

  const topCoins = coins.slice(0, 50)
  const totalPages = topCoins.length * COUNTRIES.length
  log('🔄', `Generating buy page params: ${topCoins.length} coins × ${COUNTRIES.length} countries = ${totalPages} pages`, 'blue')

  const buyPagePath = `${CONFIG.siteDir}/app/buy/[coin]/[country]/page.js`
  if (!fs.existsSync(buyPagePath)) {
    log('❌', 'buy page not found!', 'red')
    return
  }

  let content = fs.readFileSync(buyPagePath, 'utf8')

  const coinIds = topCoins.map(c => `'${c.id}'`).join(',\n    ')
  const countryIds = COUNTRIES.map(c => `'${c}'`).join(', ')

  const newStaticParams = `export async function generateStaticParams() {
  // Auto-generated by generate.js — ${new Date().toISOString()}
  // ${totalPages} pages total
  const topCoins = [
    ${coinIds}
  ]
  const countries = [${countryIds}]
  const params = []
  for (const coin of topCoins) {
    for (const country of countries) {
      params.push({ coin, country })
    }
  }
  return params
}`

  content = content.replace(
    /export async function generateStaticParams\(\) \{[\s\S]*?\n\}/,
    newStaticParams
  )

  fs.writeFileSync(buyPagePath, content, 'utf8')
  log('✅', `Buy pages: ${totalPages} static params updated`, 'green')
}

